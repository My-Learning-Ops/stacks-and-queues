





// A generic implementation of a queue data structure backed by a linked list
public class Queue<T> {

    // Represents a node in the queue
    class Node<T> {
        T value;
        Node<T> next;

        // Constructs a node with a value and link
        Node(T val, Node<T> n) {
            value = val;
            next = n;
        }
    }

    










}
